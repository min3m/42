#include <unistd.h>

int	main(int agrc, char **agrv)
{
	int	i;
	int	j;

	i = agrc - 1;
	while (i >= 1)
	{
		j = 0;
		while (agrv[i][j])
		{
			write(1, &agrv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i--;
	}
	return (0);
}
