#include <unistd.h>

void	ft_print_num(int num)
{
	if (num < 0)
	{
		if (num == -2147483648)
			write(1, "-2147483648", 11);
		else
		{
			write(1, "-", 1);
			num = -num;
		}
	}
	if (num == 0)
		return ;
	ft_print_num(num / 10);
	write(1, &"0123456789"[num % 10], 1);
}
